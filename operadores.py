w = 12
x = 5
y = 10


# Suma +

print("Suma", x + y)

# Resta -
print("Resta", y - x)

# Multiplicación *
print("Multiplicación", x * y)

# Division / (retorna un float)
print("División", y / x)

# Modulo %
print("Módulo(resto)", w % x)

# Potencia **
print("Potencia", y ** x)

# Division //
print("División entera", w // x)

par = 8
print("Es par?", par % 2 == 0)

