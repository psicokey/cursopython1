# Operadores de comparacion

x = 5
y = 3
z = 5

print(x == y)  # ¿Es igual?
print(x != y)  # ¿Es diferente?
print(x > y)  # ¿Es mayor?
print(x < y)  # ¿Es menor?
print(x >= y)  # ¿Es mayor o igual?
print(x <= z)  # ¿Es menor o igual?

# Operadores lógicos

print(x > y and y > z)  # False
print(x > y or y > z)  # True


v = True
f = False

print(not (v))
print(not (f))
